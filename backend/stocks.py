# 통합 마스터 딕셔너리: {ticker: {"name": ..., "market": ...}}
ALL_STOCKS: dict[str, dict] = {}
for _ticker, _name in KOSPI_MAJOR:
    ALL_STOCKS[_ticker] = {"name": _name, "market": "KOSPI"}
for _ticker, _name in KOSDAQ_MAJOR:
    ALL_STOCKS[_ticker] = {"name": _name, "market": "KOSDAQ"}


def get_name(ticker: str) -> str:
    """종목코드(6자리 또는 .KS/.KQ 포함)로 종목명 반환. 없으면 코드 그대로."""
    code = ticker.split(".")[0]
    return ALL_STOCKS.get(code, {}).get("name", ticker)


def get_market(ticker: str) -> str:
    """종목코드로 시장 구분(KOSPI/KOSDAQ) 반환. 없으면 빈 문자열."""
    code = ticker.split(".")[0]
    return ALL_STOCKS.get(code, {}).get("market", "")
